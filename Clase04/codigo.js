// ---------math------

//Math.pow devuelve un numero elevado al exponente deseado(num,exp)
console.log(Math.pow(8,2));

//Math.round redondea al numero mas cercano
console.log(Math.round(6.6));

//Math.floor redondea al numero mas cercano hacia abajo
console.log(Math.floor(6.6));

//Math.floor redondea al numero mas cercano hacia arriba
console.log(Math.ceil(6.6));

//Math.random devuelve un numero aleatorio del 0 al 1
//si quiero que sea mas del 1 pongo el *10 o diferente valor
console.log(Math.random()*10);
console.log(Math.ceil(Math.random()*10));



